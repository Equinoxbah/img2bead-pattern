# 程序配置区（删除预览图路径）
BEAD_WIDTH = 48    # 拼豆画布横向格子数
BEAD_HEIGHT = 48   # 拼豆画布纵向格子数
CELL_SIZE = 30     # 图纸单格像素大小
INPUT_IMG_PATH = "input.jpg"
OUTPUT_DRAWING = "bead_drawing.png"  # 仅输出这一张图纸
#---------------------------

def find_closest_bead_color(pixel_rgb):
    """加权人眼色差匹配，浮点运算消除overflow警告，返回色号+对应RGB"""
    r1, g1, b1 = map(float, pixel_rgb)
    min_dist = float("inf")
    best_code = None
    best_rgb = None
    for code, (r2, g2, b2) in BEAD_COLORS.items():
        r2, g2, b2 = map(float, (r2, g2, b2))
        dr = r1 - r2
        dg = g1 - g2
        db = b1 - b2
        dist = 0.299 * dr * dr + 0.587 * dg * dg + 0.114 * db * db
        if dist < min_dist:
            min_dist = dist
            best_code = code
            best_rgb = (int(r2), int(g2), int(b2))
    return best_code, best_rgb

def block_average_sample(origin_img, target_w, target_h):
    """分块平均像素合并，替代粗暴resize，消除原图噪点杂色"""
    src_w, src_h = origin_img.size
    img_arr = np.array(origin_img, dtype=np.float32)
    code_matrix = []
    color_matrix = []
    for y_block in range(target_h):
        y_start = int(y_block / target_h * src_h)
        y_end = int((y_block + 1) / target_h * src_h)
        if y_end == y_start:
            y_end += 1
        row_code = []
        row_color = []
        for x_block in range(target_w):
            x_start = int(x_block / target_w * src_w)
            x_end = int((x_block + 1) / target_w * src_w)
            if x_end == x_start:
                x_end += 1
            block_pixels = img_arr[y_start:y_end, x_start:x_end]
            avg_r = np.mean(block_pixels[:, :, 0])
            avg_g = np.mean(block_pixels[:, :, 1])
            avg_b = np.mean(block_pixels[:, :, 2])
            code, rgb = find_closest_bead_color((avg_r, avg_g, avg_b))
            row_code.append(code)
            row_color.append(rgb)
        code_matrix.append(row_code)
        color_matrix.append(row_color)
    return code_matrix, color_matrix

def image_preprocess(img_path):
    """图片预处理：读取→灰度降噪→转回RGB，大幅减少人像杂色"""
    img = Image.open(img_path).convert("RGB")
    gray_img = img.convert("L")
    clean_img = gray_img.convert("RGB")
    return clean_img

def count_bead_codes(code_matrix):
    """统计每个色号出现次数，用于右侧数量标注"""
    count_dict = {}
    for row in code_matrix:
        for code in row:
            count_dict[code] = count_dict.get(code, 0) + 1
    return count_dict

def draw_bead_paper(code_mat, color_mat):
    """仅生成打印图纸：网格内色号，右侧图例=色块+色号+所需数量"""
    h = len(code_mat)
    w = len(code_mat[0])
    code_count = count_bead_codes(code_mat)
    # 加宽画布适配数量文字
    total_canvas_w = w * CELL_SIZE + 320
    total_canvas_h = h * CELL_SIZE
    paper_canvas = Image.new("RGB", (total_canvas_w, total_canvas_h), "white")
    draw = ImageDraw.Draw(paper_canvas)
    # 字体兼容
    try:
        main_font = ImageFont.truetype("simhei.ttf", 13)
        title_font = ImageFont.truetype("simhei.ttf", 16)
    except:
        main_font = ImageFont.load_default()
        title_font = ImageFont.load_default()
    # 绘制网格、色块、格子内色号
    for y in range(h):
        for x in range(w):
            current_code = code_mat[y][x]
            r, g, b = color_mat[y][x]
            x1 = x * CELL_SIZE
            y1 = y * CELL_SIZE
            x2 = x1 + CELL_SIZE
            y2 = y1 + CELL_SIZE
            draw.rectangle([x1, y1, x2, y2], fill=(r, g, b), outline="gray", width=1)
            text_pos_x = x1 + CELL_SIZE // 2 - 7
            text_pos_y = y1 + CELL_SIZE // 2 - 8
            draw.text((text_pos_x, text_pos_y), current_code, fill="black", font=main_font)
    # 右侧图例标题
    legend_start_x = w * CELL_SIZE + 20
    draw.text((legend_start_x, 10), "色号 / 数量", fill="black", font=title_font)
    sorted_codes = sorted(code_count.items(), key=lambda x: x[0])
    line_spacing = 34
    for idx, (code, count) in enumerate(sorted_codes):
        ly = 35 + idx * line_spacing
        draw.rectangle([legend_start_x, ly, legend_start_x + 26, ly + 26], fill=BEAD_COLORS[code], outline="black")
        # 显示格式：色号 ×数量
        draw.text((legend_start_x + 38, ly), f"{code}  ×{count}", fill="black", font=main_font)
    paper_canvas.save(OUTPUT_DRAWING)
    print(f"拼豆打印图纸已生成：{OUTPUT_DRAWING}")
    return paper_canvas

if __name__ == "__main__":
    try:
        # 1. 图片预处理降噪
        clean_img = image_preprocess(INPUT_IMG_PATH)
        # 2. 分块平均合并像素，获取色号矩阵、颜色矩阵
        bead_code_matrix, bead_color_matrix = block_average_sample(clean_img, BEAD_WIDTH, BEAD_HEIGHT)
        # 3. 只生成带色号+数量的打印图纸（删除预览图生成代码）
        draw_bead_paper(bead_code_matrix, bead_color_matrix)
        print("===== 转换完成，仅输出拼豆图纸 =====")
    except FileNotFoundError:
        print(f"错误：未找到图片文件 {INPUT_IMG_PATH}，请将图片放置程序同级目录并重命名为 input.jpg")
    except Exception as err:
        print(f"程序运行异常：{err}")
